def __init__(self, longeur, largeur):
    self.longeur = longeur
    self.largeur = largeur


def calcul_aire(self , aire):
    aire = self.longeur * self.largeur
    return aire


def afficher_infos(self):

    print("\n la longeur de rectangle est de {self.longeur()}"
          "\n la largeur de rectangle est de {self.largeur()}"
          "\n l'aire de rectangle est de {self.calcul_aire()}")


